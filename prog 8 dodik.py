# ARITMATIKA (X : + - ** % //)

a = 24
b = 40

# penjumlahan
hasil = a + b
print ("nilai dari a + b = ", hasil)

# perpangkatan
hasil = a ** b
print ("nilai dari a ** b = ", hasil)

# modulus
hasil = a % b
print ("nilai dari a mod b = ", hasil)

#floor dividion (pembagian terdekat-kebawah)
hasil = a // b
print ("nilai dari a flor b = ", hasil)